    private void editShowPassMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editShowPassMouseClicked
        if (editShowPass.getText().compareTo("Show password") == 0) {
            editPassTxt.setText(item.getPassword());
            editRepassTxt.setText(item.getPassword());
            editShowPass.setText("Hide password");
        } else {
            editPassTxt.setText("Hidden password!");
            editRepassTxt.setText("Hidden password!");
            editShowPass.setText("Show password");
        }
    }//GEN-LAST:event_editShowPassMouseClicked

    private void editDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editDeleteBtnActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Would you like to delete this User Information?", "Warning", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            Main.dlm.remove(Main.selectedItem);
            DbController.RemoveInfoFromDB(item);
            setVisible(false);
        }
    }//GEN-LAST:event_editDeleteBtnActionPerformed

