    private void editSaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editSaveBtnActionPerformed
        if (item.getPassword().compareTo(editPassTxt.getText()) != 0) {
            if (editPassTxt.getText().compareTo(editRepassTxt.getText()) == 0) {
                User temp = new User();
                temp.setUsername(editUsernameTxt.getText());
                temp.setPassword(editPassTxt.getText());
                temp.setUrl(editUrlTxt.getText());
                temp.setNote(editNoteTxt.getText());

                DbController.UpdateInfoToDB(item, temp);
                Main.dlm.setElementAt(temp, Main.selectedItem);
                setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "New password is not match.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            setVisible(false);
        }
    }//GEN-LAST:event_editSaveBtnActionPerformed

